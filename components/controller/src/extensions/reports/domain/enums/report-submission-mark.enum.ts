/**
 * Пользовательская отметка на ячейке календаря отчётности.
 *
 * Пока используется один вариант — NOT_REQUIRED («не надо сдавать»).
 * Enum оставлен расширяемым: в будущем сюда можно добавить, напр.,
 * SUBMITTED_EXTERNALLY (сдали в бумаге/через стороннюю систему —
 * файл в нашем архиве отсутствует, но ячейка не overdue).
 *
 * В календаре приоритет статусов: submitted > draft > not_required > overdue > empty.
 * NOT_REQUIRED сильнее overdue (чтобы серый не перекрашивался в красный),
 * но слабее submitted/draft (фактическая сдача или черновик переопределяют отметку).
 */
export enum ReportSubmissionMark {
  NOT_REQUIRED = 'not_required',
}
