/**
 * Очищает технические детали ассертов смарт-контрактов из текста ошибки
 * перед показом пользователю.
 *
 * Контракты бросают ассерты вида
 *   `walletop TRANSFER: недостаточно средств на кошельке w.wal.share`
 * — пользователю не нужны ни scope-префикс операции (`walletop TRANSFER:`),
 * ни служебное имя кошелька (`w.wal.share`). Он нажал конкретную кнопку и так
 * понимает, о каком кошельке речь. Ему достаточно «Недостаточно средств на
 * кошельке».
 *
 * Полный технический текст при этом остаётся доступен разработчикам: он
 * пишется в логи контроллера (graphql-exceptions.filter) и дублируется в
 * консоль браузера в `FailAlert`.
 *
 * Идемпотентна и безопасна для нетехнических сообщений: если в тексте нет
 * технических токенов — возвращает его без изменений (кроме нормализации
 * пробелов и заглавной первой буквы при снятом префиксе).
 *
 * @param message — сырой текст ошибки
 * @returns человеко-читаемый текст без технических деталей
 */
export const sanitizeBlockchainError = (message: string): string => {
  if (!message) return message;

  let out = message;

  // 1. Снять ведущий технический scope-префикс ассерта. Распознаём два вида
  //    scope, чтобы не зацепить обычное русское «Слово: ...»:
  //    A) идентификатор с разделителями `.`/`::` — `ledger2::apply`,
  //       `o.mkt.supply`, `w.wal.share`;
  //    B) слово + ALLCAPS-опкод — `walletop TRANSFER`, `walletop BLOCK`.
  //    Кириллица под `[a-z]` не попадает, поэтому пользовательские
  //    русскоязычные сообщения не трогаются.
  out = out.replace(
    /^(?:[a-z][a-z0-9_]*(?:[.:]{1,2}[a-z0-9_]+)+|[a-z][a-z0-9_]*\s[A-Z][A-Z0-9_]+):\s*/,
    '',
  );

  // 2. Убрать служебные имена ledger2-кошельков (`w.wal.share`,
  //    `w.mkt.payout`, `w.sov.infra`, `w.cap.gen`…) вместе с предшествующим
  //    пробелом — чтобы осталось «…на кошельке», без болтающегося имени.
  out = out.replace(/\s*\bw\.[a-z]{2,4}\.[a-z0-9]{2,12}\b/g, '');

  // 3. Нормализовать пробелы (схлопнуть двойные, убрать перед пунктуацией).
  out = out
    .replace(/\s{2,}/g, ' ')
    .replace(/\s+([.,;:!?])/g, '$1')
    .trim();

  // 4. Заглавная первая буква — после снятия префикса часто остаётся строчная.
  if (out) out = out.charAt(0).toUpperCase() + out.slice(1);

  return out;
};
