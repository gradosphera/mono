/**
 * Props платформенного UI-гейта оферты расширения.
 *
 * Используется на страницах активности расширения (например,
 * /capital/buy → программа Благорост) — рендерит дочерний slot
 * только если пайщик уже подписал указанную оферту on-chain.
 */
export interface OfferGateProps {
  /** Кооператив, в котором проверяется подпись (текущий coopname). */
  coopname: string;

  /** Имя пользователя (current signed-in пайщик). */
  username: string;

  /**
   * Тип оферты для проверки on-chain `agreements.type`
   * (например 'blagorost' для программы Благорост,
   * 'generator' для программы Генерация).
   */
  agreementType: string;

  /**
   * Понятное название оферты для UI-блокера
   * (например «Благорост», «Стол заказов»).
   */
  offerTitle: string;

  /**
   * URL, куда вести пайщика, если оферта не подписана —
   * обычно страница регистрации/онбординга с этой офертой.
   * Если не задан — кнопка ведёт в /signup.
   */
  signupUrl?: string;
}
