/**
 * @brief Отправляет уведомление о переходе участника на новый уровень
 * Inline action для уведомления системы о достижении участником нового уровня:
 * - Вызывается автоматически при достижении энергии 100%
 * - Содержит информацию о предыдущем и новом уровне
 * - Может использоваться внешними системами для отправки уведомлений
 * @param coopname Имя кооператива
 * @param username Имя участника, достигшего нового уровня
 * @param prev_level Предыдущий уровень участника
 * @param new_level Новый уровень участника
 * @ingroup public_actions
 * @ingroup public_capital_actions
 *
 * @note Авторизация требуется от аккаунта: @p _capital (вызывается inline action)
 */
void capital::lvlnotify(eosio::name coopname, eosio::name username, uint32_t prev_level, uint32_t new_level) {
  require_auth(_capital);
  
  // Это уведомление может быть перехвачено внешними системами
  // Контроллер может слушать это действие и отправлять уведомления пользователям
  
  // Логирование для истории
  eosio::print("Участник ", username, " кооператива ", coopname, 
               " перешел с уровня ", prev_level, " на уровень ", new_level);
}

